/**
 * @author SNIPPIK
 * @description Абстрактный базовый класс для всех слоёв транспортного протокола голосового соединения.
 *              Реализует паттерн «Шаблонный метод» (Template Method) и служит основой для:
 *              - UDPLayer (UDP сокет, discovery handshake)
 *              - RTPLayer (формирование RTP-заголовков, AES-GCM шифрование)
 *              - DAVELayer (MLS сквозное шифрование, управление групповыми ключами)
 *
 * @remarks
 * Класс предоставляет общий интерфейс и стандартные заглушки для методов, которые должны быть
 * переопределены в наследниках. Поле `_client` хранит специфичный для слоя клиентский объект
 * (например, VoiceUDPSocket, RTPCipher или DAVESession).
 *
 * @typeParam T - Тип клиентского объекта, с которым работает конкретный слой.
 * @see UDPLayer, RTPLayer, DAVELayer
 */
export abstract class BaseLayer<T> {
    /**
     * @description Максимальное количество повторных попыток для операций, требующих ретраев
     *              (например, отправка discovery-пакетов или повторное шифрование).
     * @static
     */
    public static MAX_RETRIES = 3;

    /**
     * @description Экземпляр специализированного клиента для данного слоя.
     *              Доступен только внутри класса и наследников.
     * @protected
     */
    protected _client: T;

    /**
     * @description Индикатор готовности слоя к работе.
     *              По умолчанию возвращает `false`. Наследники должны переопределить этот геттер,
     *              возвращая `true`, когда слой полностью инициализирован (например, установлено
     *              UDP-соединение, загружены ключи RTP или готова MLS-сессия).
     *
     * @returns {boolean} Готовность слоя.
     */
    public get ready(): boolean {
        return false;
    };

    /**
     * @description Создаёт и инициализирует слой с переданными параметрами.
     *              Абстрактный метод-заглушка. Наследники обязаны переопределить его
     *              для конкретной логики (поднятие UDP, инициализация шифров, создание сессии DAVE).
     *
     * @param _ - Аргументы, зависящие от слоя (например, объект `ready` из WebSocket,
     *                        версия протокола и т.д.).
     * @throws {TypeError} При вызове не переопределённого метода.
     */
    public create = (..._: any[]): void => {
        throw new TypeError("Not found create function");
    };

    /**
     * @description Уничтожает слой, освобождая все ресурсы: закрывает сокеты, сбрасывает ключи,
     *              останавливает фоновые циклы (если есть). Наследники обязаны переопределить этот метод.
     *
     * @throws {TypeError} При вызове не переопределённого метода.
     */
    public destroy = (): void => {
        throw new TypeError("Not found destroy function");
    };
}