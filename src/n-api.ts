const Native = require('../native/index.js') as any; // или точный путь к .node

/**
 * Универсальный тип для нативного класса по интерфейсу:
 * - T — интерфейс с сигнатурой constructor
 * - NativeClass<T> — конструктор с правильными аргументами
 * - Экземпляр класса можно получить через InstanceOf<T>
 */
type NativeClass<T extends { constructor?: (...args: any[]) => any }> =
    T extends { constructor: (...args: infer A) => any }
        ? new (...args: A) => T
        : new (...args: any[]) => T;

/**
 * Универсальный алиас для удобного использования:
 * iType<typeof NativeTyped.AudioEngine> => тип экземпляра AudioEngine
 */
export type iType<T> = T extends new (...args: any[]) => infer R ? R : never;

/**
 * @module native-bindings
 * @author SNIPPIK
 * @description Высокопроизводительные нативные привязки на Rust для работы с медиа-потоками,
 * сетью, точным таймингом, парсингом OGG/Opus и управлением аудио-очередями.
 *
 * Все классы создаются через `new`, большинство методов синхронные.
 * Асинхронность достигается через колбэки (pipeStdout, startListening, startCycle).
 * Ошибки выбрасываются как экземпляры Error с полем code и message из нативного слоя.
 */

/**
 * @support Тип пакета, обнаруженного парсером OGG/Opus.
 * - head  → OpusHead (основные параметры кодека: каналы, частота дискретизации)
 * - tags  → OpusTags (метаданные: название, артист, комментарии)
 * - frame → аудиофрейм Opus (собственно звук)
 * @type OpusPacketType
 * @extends OggOpusParser
 */
export type OpusPacketType = 'head' | 'tags' | 'frame';

/* ────────────────────────────────────────────────
   Интерфейсы классов, экспортируемых из нативного модуля
───────────────────────────────────────────────── */

/**
 * @description Нативный парсер контейнера OGG с кодеком Opus.
 * Работает потоково: принимает произвольные куски байтов и выдаёт полные Opus-пакеты.
 * @support Подходит для:
 * - потоков из FFmpeg
 * - RTP-пакетов с OGG-инкапсуляцией
 * - файлов .ogg
 * @interface OggOpusParser
 */
export interface iOggOpusParser {
    constructor(): void;

    /**
     * Основной метод парсинга.
     * Добавляет новые данные в буфер и пытается извлечь полные страницы и пакеты.
     * При обнаружении полного Opus-пакета **синхронно** вызывает callback.
     *
     * @param chunk - произвольный фрагмент байтов (может быть неполной страницей или несколькими)
     * @param callback - вызывается сразу при нахождении пакета (не асинхронно!)
     * @param callback.type - тип пакета
     * @param callback.data - полные байты пакета (можно сразу передать в opus-decoder)
     */
    parse(chunk: Buffer, callback: (type: OpusPacketType, data: Buffer) => void): void;

    /**
     * Полностью очищает внутренние буферы (remainder и packet_carry),
     * сбрасывает состояние (серийный номер потока, флаг ожидания заголовка).
     * Вызывать обязательно при:
     * - смене источника потока
     * - переподключении к голосовому каналу
     * - завершении сессии
     */
    destroy(): void;
}

/**
 * @description Управляемый процесс FFmpeg, запущенный из нативного кода.
 * Процесс работает в отдельном потоке, не блокирует Event Loop Node.js.
 * Stdout читается асинхронно и передаётся через колбэк.
 * @interface FfmpegProcess
 */
export interface iFfmpegProcess {
    constructor(args: Array<string>, name: string): void

    /**
     * Запускает чтение stdout и передачу данных в JS.
     * Вызывается один раз после создания экземпляра.
     *
     * @param callback - основной колбэк: вызывается для каждого чанка stdout (обычно OGG/Opus)
     */
    pipeStdout(callback: (pul: {
        type: OpusPacketType,
        data: Buffer
    }[]) => any): void

    /**
     * Принудительно завершает процесс FFmpeg (посылает SIGKILL).
     * Освобождает все ресурсы (память, потоки, дескрипторы).
     * После вызова объект становится невалидным — повторный вызов pipeStdout приведёт к ошибке.
     */
    destroy(): void;
}

/**
 * Структура, представляющая движок аудио-буфера.
 * Хранит пакеты аудиоданных (например, кадры Opus) в виде очереди.
 * Позволяет добавлять, читать и просматривать пакеты, отслеживать позицию чтения.
 */
export interface iAudioEngine {
    /**
     * @description Возвращает последний пакет в очереди (без удаления).
     * @public
     */
    get lastPacket(): Buffer | null;

    /**
     * Получает следующий пакет из очереди (удаляя его) и увеличивает позицию чтения.
     * Возвращает `Option<Buffer>`, который можно передать обратно в JavaScript.
     * @public
     */
    get packet(): Buffer | null;

    /**
     * @description Возвращает текущее количество пакетов в буфере.
     * @public
     */
    get size(): number;

    /**
     * @description Возвращает текущую позицию чтения (количество извлечённых пакетов).
     * @public
     */
    get position(): number;

    /**
     * @description Устанавливает позицию чтения вручную (может использоваться для перемотки).
     * @public
     */
    set position(pos: number);

    /**
     * Создаёт новый экземпляр AudioEngine.
     *
     * # Аргументы
     * * `max_minutes` – максимальное количество минут аудио, которое может храниться в буфере.
     *
     * # Расчёт ёмкости
     * Исход из того, что в секунду передаётся 50 пакетов (например, для 20 мс кадров Opus).
     * Ёмкость = 50 пакетов/сек * 60 сек * max_minutes.
     * Минимальная ёмкость – 1000 пакетов.
     */
    constructor(maxMinutes: number): void;

    /**
     * Добавляет один пакет в очередь.
     * Если буфер переполнен (достигнут лимит по времени), удаляется самый старый пакет (FIFO).
     *
     * # Аргументы
     * * `packet` – бинарный буфер с аудиоданными (из Node.js).
     */
    addPacket(packet: Buffer): void;

    /**
     * Добавляет несколько пакетов в очередь.
     *
     * # Аргументы
     * * `packets` – массив бинарных буферов (из Node.js)
     */
    addPackets(packets: Array<Buffer>): void;

    /**
     * Получает несколько пакетов подряд (удаляя их) и увеличивает позицию на соответствующее количество.
     *
     * # Аргументы
     * * `count` – максимальное количество пакетов для получения.
     *
     * # Возвращает
     * Вектор Buffer (может быть меньше запрошенного, если пакетов недостаточно).
     */
    getPackets(count: number): Array<Buffer>

    /**
     * Получает пакет по индексу без удаления (прямой доступ).
     * Индексация с 0.
     */
    getPacketAt(idx: number): Buffer | null;

    /**
     * @description Полностью очищает буфер и сбрасывает позицию в 0.
     * @public
     */
    clear(): void;
}

/**
 * Буферизованный UDP-сокет, доступный из JavaScript через N-API.
 *
 * Предоставляет методы для отправки дейтаграмм (с буферизацией), получения входящих
 * пакетов через вызов, а также статистику. Интегрируется с глобальным балансировщиком
 * CycleManager для регулярного вызова tick(), который отправляет накопленные пакеты.
 *
 * Конструктор создаёт сокет, привязывается к случайному порту, подключается к указанному
 * удалённому адресу и регистрирует себя в глобальном балансировщике для автоматической
 * отправки.
 */
export interface iUDPSocket {
    /**
     * @description Текущее количество пакетов в очереди на отправку.
     * @public
     */
    get packets(): number;

    /**
     * @description Количество пакетов, сброшенных из-за переполнения очереди или временных ошибок.
     * @public
     */
    get drops(): number;

    /**
     * Создаёт новый UDP-сокет, подключается к указанному удалённому адресу и
     * регистрируется в глобальном балансировщике.
     *
     * # Аргументы
     * * `remote_addr` - адрес удалённого хоста в формате "ip:port" (например, "127.0.0.1:12345").
     *
     * Сокет создаётся с неблокирующим режимом (set_nonblocking(true)), чтобы операции
     * send/recv не блокировали поток.
     */
    constructor(remoteAddr: string): void;

    /**
     * Добавляет пакет в очередь на отправку. С проверкой мусора
     *
     * # Аргументы
     * * `packet` - Buffer с данными для отправки.
     *
     * Если длина пакета будет равна 3, он будет проверен, допустим ли он!
     * Если размер пакета более 3, то позволяем ему отправится в циклической системе
     */
    pushPacket(packet: Buffer): void;

    /**
     * Добавляет пакеты в очередь на отправку. С проверкой мусора
     *
     * # Аргументы
     * * `packets` - Массив Buffer с данными для отправки.
     *
     * Если длина пакета будет равна 3, он будет проверен, допустим ли он!
     * Если размер пакета более 3, то позволяем ему отправится в циклической системе
     */
    pushPackets(packets: Array<Buffer>): void;

    /**
     * Начинает прослушивание входящих пакетов в отдельном потоке.
     *
     * # Аргументы
     * * `callback` - JS-функция, которая будет вызываться при получении каждого пакета.
     *   Функция получает один аргумент — Buffer с данными.
     *
     * Если прослушивание уже активно, метод ничего не делает.
     * Поток работает, пока не будет вызван `stop_listening` или уничтожен объект.
     * Для вызова из фонового потока используется ThreadsafeFunction.
     * @public
     */
    startListening(callback: (...args: any[]) => any): void;

    /**
     * @description Останавливает прослушивание входящих пакетов и дожидается завершения потока.
     * @public
     */
    stopListening(): void;

    /**
     * Полностью уничтожает сессию: останавливает прослушивание, очищает очередь,
     * удаляет себя из глобального балансировщика. Повторные вызовы игнорируются.
     * @public
     */
    destroy(): void;
}

/**
 * Буферизованный RTP-сокет для голоса с шифрованием AES-256-GCM.
 *
 * Генерирует RTP-пакеты с правильными заголовками (sequence, timestamp, SSRC),
 * шифрует полезную нагрузку (Opus-фрейм) с использованием режима AEAD_AES_256_GCM_RTPSIZE,
 * который требует включения заголовка RTP в дополнительные аутентифицированные данные (AAD).
 *
 * Счётчики sequence, timestamp и nonce counter управляются атомарно и потокобезопасны.
 */
export interface VoiceRTPSocket {
    /**
     * @description Возвращает строку, идентифицирующую режим шифрования.
     * @public
     */
    get mode(): "aead_aes256_gcm_rtpsize"

    /**
     * Возвращает текущий nonce (12 байт) и его первые 4 байта (tail).
     *
     * Nonce формируется как:
     * - первые 4 байта: значение счётчика `counter` (увеличивается при каждом вызове)
     * - остальные 8 байт: нули (согласно спецификации Discord/RTP)
     */
    get nonce(): Array<Buffer>;

    /**
     * Создаёт новый экземпляр VoiceRTPSocket.
     *
     * # Аргументы
     * * `ssrc` - 32-битный идентификатор источника синхронизации (SSRC), уникальный для потока.
     * * `key` - Buffer длиной ровно 32 байта, содержащий ключ AES-256-GCM.
     *
     * Начальные значения sequence, timestamp и counter генерируются случайным образом
     * для улучшения криптостойкости и предотвращения угадывания.
     */
    constructor(ssrc: number, key: Uint8Array): void

    /**
     * Создаёт зашифрованный RTP-пакет из переданного Opus-фрейма.
     *
     * Структура пакета (RFC 3550 + Discord AEAD AES256-GCM RTP Size):
     * - RTP Header (12 байт): V=2, P=0, X=0, CC=0, M=0, PT=120, SEQ, TS, SSRC
     * - Encrypted Payload (N байт)
     * - Authentication Tag (16 байт)
     * - Nonce Tail (4 байта): первые 4 байта nonce в конце пакета
     */
    packet(frame: Buffer): Buffer;

    /**
     * @description Сбрасывает все внутренние счётчики в ноль.
     * @public
     */
    destroy(): void
}

