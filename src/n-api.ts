/**
 * @description Нативный парсер контейнера OGG с кодеком Opus.
 * Принимает произвольные куски байтов и выдаёт полные Opus-пакеты.
 * @support Подходит для:
 * - потоков из FFmpeg
 * - RTP-пакетов с OGG-инкапсуляцией
 * - файлов .ogg
 * @interface OggOpusParser
 */
export interface iOggOpusParser {
    constructor(): void;

    /**
     * Основной метод парсинга.
     * Добавляет новые данные в буфер и пытается извлечь полные страницы и пакеты.
     * При обнаружении полного Opus-пакета **синхронно** вызывает callback.
     *
     * @param chunk - произвольный фрагмент байтов (может быть неполной страницей или несколькими)
     * @param callback - вызывается сразу при нахождении пакета (не асинхронно!)
     * @param callback.type - тип пакета
     * @param callback.data - полные байты пакета (можно сразу передать в opus-decoder)
     */
    parse(chunk: Buffer, callback: (type: OpusPacketType, data: Buffer) => void): void;

    /**
     * Полностью очищает внутренний буфер (remainder и packet_carry),
     * сбрасывает состояние (серийный номер потока, флаг ожидания заголовка).
     * Вызывать обязательно при:
     * - смене источника потока
     * - переподключении к голосовому каналу
     * - завершении сессии
     */
    destroy(): void;
}

/**
 * @description Управляемый процесс FFmpeg, запущенный из нативного кода.
 * Процесс работает в отдельном потоке, не блокирует Event Loop Node.js.
 * Stdout читается асинхронно и передаётся через функцию.
 * @interface FfmpegProcess
 */
export interface iFfmpegProcess {
    constructor(args: Array<string>, name: string): void;

    /**
     * Запускает чтение stdout и передачу данных в JS.
     * Вызывается один раз после создания экземпляра.
     *
     * @param callback - основная функция: вызывается для каждого чанка stdout (Opus)
     */
    pipeStdout(callback: (pul: Buffer[]) => any): void;

    /**
     * Принудительно завершает процесс FFmpeg (посылает SIGKILL).
     * Освобождает все ресурсы (память, потоки, дескрипторы).
     * После вызова объект становится невалидным — повторный вызов pipeStdout приведёт к ошибке.
     */
    destroy(): void;
}

/**
 * Структура, представляющая движок аудио-буфера.
 * Хранит пакеты аудиоданных (например, кадры Opus) в виде очереди.
 * Позволяет добавлять, читать и просматривать пакеты, отслеживать позицию чтения.
 */
export interface iAudioEngine {
    /**
     * @description Возвращает последний пакет в очереди (без удаления).
     * @public
     */
    get lastPacket(): Buffer | null;

    /**
     * Получает следующий пакет из очереди (удаляя его) и увеличивает позицию чтения.
     * Возвращает `Option<Buffer>`, который можно передать обратно в JavaScript.
     * @public
     */
    get packet(): Buffer | null;

    /**
     * @description Возвращает текущее количество пакетов в буфере.
     * @public
     */
    get size(): number;

    /**
     * @description Возвращает текущую позицию чтения (количество извлечённых пакетов).
     * @public
     */
    get position(): number;

    /**
     * @description Устанавливает позицию чтения вручную (может использоваться для перемотки).
     * @public
     */
    set position(pos: number);

    /**
     * Создаёт новый экземпляр AudioEngine.
     *
     * # Аргументы
     * * `max_minutes` – максимальное количество минут аудио, которое может храниться в буфере.
     *
     * # Расчёт ёмкости
     * Исход из того, что в секунду передаётся 50 пакетов (например, для 20 мс кадров Opus).
     * Ёмкость = 50 пакетов/сек * 60 сек * max_minutes.
     * Минимальная ёмкость – 1000 пакетов.
     */
    constructor(maxMinutes: number): void;

    /**
     * Добавляет один пакет в очередь.
     * Если буфер переполнен (достигнут лимит по времени), удаляется самый старый пакет (FIFO).
     *
     * # Аргументы
     * * `packet` – бинарный буфер с аудиоданными (из Node.js).
     */
    addPacket(packet: Buffer): void;

    /**
     * Добавляет несколько пакетов в очередь.
     *
     * # Аргументы
     * * `packets` – массив бинарных буферов (из Node.js)
     */
    addPackets(packets: Array<Buffer>): void;

    /**
     * Получает несколько пакетов подряд (удаляя их) и увеличивает позицию на соответствующее количество.
     *
     * # Аргументы
     * * `count` – максимальное количество пакетов для получения.
     *
     * # Возвращает
     * Вектор Buffer (может быть меньше запрошенного, если пакетов недостаточно).
     */
    getPackets(count: number): Array<Buffer>;

    /**
     * Получает пакет по индексу без удаления (прямой доступ).
     * Индексация с 0.
     */
    getPacketAt(idx: number): Buffer | null;

    /**
     * @description Полностью очищает буфер и сбрасывает позицию в 0.
     * @public
     */
    clear(): void;
}

/**
 * Буферизованный UDP-сокет, доступный из JavaScript через N-API.
 *
 * Предоставляет методы для отправки дейтаграмм (с буферизацией), получения входящих
 * пакетов через вызов, а также статистику. Интегрируется с глобальным балансировщиком
 * CycleManager для регулярного вызова tick(), который отправляет накопленные пакеты.
 *
 * Конструктор создаёт сокет, привязывается к случайному порту, подключается к указанному
 * удалённому адресу и регистрирует себя в глобальном балансировщике для автоматической
 * отправки.
 */
export interface iUDPSocket {
    /**
     * @description Текущее количество пакетов в очереди на отправку.
     * @public
     */
    get packets(): number;

    /**
     * @description Количество пакетов, сброшенных из-за переполнения очереди или временных ошибок.
     * @public
     */
    get drops(): number;

    /**
     * Создаёт новый UDP-сокет, подключается к указанному удалённому адресу и
     * регистрируется в глобальном балансировщике.
     *
     * # Аргументы
     * * `remote_addr` - адрес удалённого хоста в формате "ip:port" (например, "127.0.0.1:12345").
     *
     * Сокет создаётся с неблокирующим режимом (set_nonblocking(true)), чтобы операции
     * send/recv не блокировали поток.
     */
    constructor(remoteAddr: string): void;

    /**
     * Добавляет пакет в очередь на отправку. С проверкой мусора
     *
     * # Аргументы
     * * `packet` - Buffer с данными для отправки.
     *
     * Если длина пакета будет равна 3, он будет проверен, допустим ли он!
     * Если размер пакета более 3, то позволяем ему отправится в циклической системе
     */
    pushPacket(packet: Buffer): void;

    /**
     * Добавляет пакеты в очередь на отправку. С проверкой мусора
     *
     * # Аргументы
     * * `packets` - Массив Buffer с данными для отправки.
     *
     * Если длина пакета будет равна 3, он будет проверен, допустим ли он!
     * Если размер пакета более 3, то позволяем ему отправится в циклической системе
     */
    pushPackets(packets: Array<Buffer>): void;

    /**
     * Начинает прослушивание входящих пакетов в отдельном потоке.
     *
     * # Аргументы
     * * `callback` - JS-функция, которая будет вызываться при получении каждого пакета.
     *   Функция получает один аргумент — Buffer с данными.
     *
     * Если прослушивание уже активно, метод ничего не делает.
     * Поток работает, пока не будет вызван `stop_listening` или уничтожен объект.
     * Для вызова из фонового потока используется ThreadsafeFunction.
     * @public
     */
    startListening(callback: (...args: any[]) => any): void;

    /**
     * @description Останавливает прослушивание входящих пакетов и дожидается завершения потока.
     * @public
     */
    stopListening(): void;

    /**
     * Полностью уничтожает сессию: останавливает прослушивание, очищает очередь,
     * удаляет себя из глобального балансировщика. Повторные вызовы игнорируются.
     * @public
     */
    destroy(): void;
}

/**
 * Объект RTP-сокета для голоса, доступный из JavaScript.
 * Выполняет шифрование аудиофреймов (Opus) в соответствии с требованиями Discord.
 *
 * # Атомарные счётчики
 * - `sequence` – 16-битный счётчик RTP-пакетов (оборачивается).
 * - `timestamp` – 32-битная метка времени, увеличивается на `TIMESTAMP_INC` для каждого пакета.
 * - `counter` – 32-битный счётчик nonce (используется как первые 4 байта 12-байтового nonce).
 *
 * # Потокобезопасность
 * Все методы могут вызываться из разных потоков благодаря атомарным операциям.
 * Однако `cipher` внутри не является `Sync`, поэтому экземпляр `VoiceRTPSocket` не должен
 * использоваться из нескольких потоков одновременно (если только не обёрнут в Mutex).
 */
export interface iVoiceRTPSocket {
    /**
     * @description Возвращает строку, идентифицирующую режим шифрования.
     * @public
     */
    get mode(): "aead_aes256_gcm_rtpsize";

    /**
     * Возвращает текущий nonce (12 байт) и его первые 4 байта (tail).
     *
     * Nonce формируется как:
     * - первые 4 байта: значение счётчика `counter` (увеличивается при каждом вызове)
     * - остальные 8 байт: нули (согласно спецификации Discord/RTP)
     */
    get nonce(): Array<Buffer>;

    /**
     * Создаёт новый экземпляр VoiceRTPSocket.
     *
     * # Аргументы
     * * `ssrc` - 32-битный идентификатор источника синхронизации (SSRC), уникальный для потока.
     * * `key` - Buffer длиной ровно 32 байта, содержащий ключ AES-256-GCM.
     *
     * Начальные значения sequence, timestamp и counter генерируются случайным образом
     * для улучшения криптостойкости и предотвращения угадывания.
     */
    constructor(ssrc: number, key: Uint8Array): void;

    /**
     * Шифрует один аудио фрейм (Opus) и возвращает полный RTP-пакет.
     *
     * # Процесс
     * 1. Формируется RTP-заголовок (12 байт) с текущими значениями sequence, timestamp, SSRC.
     * 2. Генерируется 12-байтовый nonce: первые 4 байта – счётчик (big-endian), остальные – нули.
     * 3. Шифруется фрейм с использованием AAD = RTP-заголовок.
     * 4. К результату добавляются первые 4 байта nonce (tail) для возможности дешифровки.
     *
     * # Формат выходного пакета
     * `[RTP header 12 байт][зашифрованные данные + 16 байт тега][4 байта tail]`
     *
     * # Ошибки
     * - Если шифрование провалилось (например, из-за неправильного nonce).
     * - Если размер фрейма превышает допустимый (проверка отсутствует, но можно добавить).
     */
    packet(frame: Buffer): Buffer;

    /**
     * Пакетное шифрование нескольких фреймов.
     * Удобно для отправки нескольких аудиопакетов за раз (снижает количество вызовов через FFI).
     *
     * # Реализация
     * Просто последовательно вызывает `packet` для каждого фрейма.
     * Аллокация результата происходит один раз с предварительным резервированием ёмкости.
     */
    packets(frames: Array<Buffer>): Array<Buffer>;

    /**
     * @description Сбрасывает все внутренние счётчики в ноль.
     * @public
     */
    destroy(): void;
}

/**
 * DAVESession — основная сессия для протокола MLS (Messaging Layer Security), используемая в Discord для E2EE.
 * Предоставляет методы для управления групповыми ключами, шифрования/расшифрования аудио пакетов (Opus/Video) и обработки proposals.
 *
 * @remarks
 * - Сессия инициализируется с версией протокола, ID пользователя, ID канала и опциональной парой ключей подписи.
 * - После инициализации может быть получен KeyPackage для отправки другим участникам.
 * - Для работы необходимо вызвать `setExternalSender` и обработать proposals/commit/welcome.
 * - Поддерживается режим passthrough (прозрачный пропуск пакетов) с опциональным тайм-аутом.
 * - Ведётся статистика шифрования/расшифрования.
 *
 * @see https://github.com/discord/dave
 */
export interface iDAVESession {
    /**
     * Создаёт новую сессию Dave с указанной версией протокола, идентификаторами пользователя и канала.
     *
     * # Аргументы
     * * `protocol_version` - Версия протокола MLS (должна быть >= 1). Обычно используется 1.
     * * `user_id` - Snowflake пользователя (например, "123456789012345678").
     * * `channel_id` - Snowflake голосового канала.
     * * `key_pair` - Опциональная пара ключей подписи. Если не указана, будет сгенерирована автоматически.
     *
     * # Ошибки
     * - Если `protocol_version == 0`
     * - Если `user_id` или `channel_id` не являются корректными числами
     * - Если внутренняя инициализация `Davey` не удалась (например, неверные параметры)
     */
    constructor(protocolVersion: number, userId: string, channelId: string, keyPair?: SigningKeyPair | undefined | null): void;

    /**
     * Переинициализирует существующую сессию новыми параметрами.
     *
     * Позволяет изменить пользователя, канал или версию протокола без создания нового объекта.
     * Все предыдущие состояния (ключи, proposals, статистика) сбрасываются.
     *
     * # Аргументы
     * Те же, что и в конструкторе.
     *
     * # Ошибки
     * Аналогичны конструктору.
     */
    reinit(protocolVersion: number, userId: string, channelId: string, keyPair?: SigningKeyPair | undefined | null): void;

    /**
     * Полностью сбрасывает состояние сессии, как после вызова конструктора.
     *
     * Все накопленные ключи, состояние группы, proposals и статистика удаляются.
     * Сессия становится непригодной для шифрования/расшифрования до повторной инициализации.
     */
    reset(): void;

    /** Возвращает текущую версию протокола, с которой работает сессия. */
    get protocolVersion(): number;

    /**
     * Указывает, готова ли сессия к выполнению шифрования/расшифрования.
     *
     * Для готовности обычно требуется успешно обработать начальный `commit` или `welcome`.
     */
    get ready(): boolean;

    /**
     * Возвращает внутренний статус сессии в виде числа.
     *
     * Значения определяются реализацией `Davey`. Обычно:
     * - `0` — инициализация
     * - `1` — готова
     * - `2` — ошибка
     */
    get status(): number;

    /**
     * Генерирует и возвращает сериализованный `KeyPackage`.
     *
     * `KeyPackage` — это структура, содержащая публичный ключ и метаданные участника.
     * Он должен быть передан другим членам группы, чтобы они могли шифровать данные для этого пользователя.
     *
     * # Возвращает
     * Буфер с данными `KeyPackage` в формате MLS.
     */
    getSerializedKeyPackage(): Buffer;

    /**
     * Устанавливает данные внешнего отправителя (`External Sender`).
     *
     * Внешний отправитель позволяет сессии принимать коммиты от сущностей, не входящих в группу
     * (например, ботов или системных аккаунтов). Без этого вызова `process_commit` для внешних коммитов будет падать.
     *
     * # Аргументы
     * * `data` - Сериализованные данные внешнего отправителя (обычно получаются из Discord API).
     */
    setExternalSender(data: Buffer): void;

    /**
     * Включает или выключает режим прозрачного пропуска пакетов (`passthrough`).
     *
     * В этом режиме шифрование и расшифрование отключаются — пакеты передаются без изменений.
     * Полезно для отладки или при временных проблемах с ключами.
     *
     * # Аргументы
     * * `enabled` - Включить (`true`) или выключить (`false`) режим.
     * * `expiry` - Опциональное время жизни режима в секундах. Если указано, режим автоматически выключится по истечении.
     */
    setPassthroughMode(enabled: boolean, expiry?: number | undefined | null): void;

    /**
     * Обрабатывает proposals (предложения об изменении группы).
     *
     * Proposals могут включать добавление/удаление участников, обновление ключей и другие операции.
     * Метод возвращает `commit`, который необходимо применить, и опциональный `welcome` для новых участников.
     *
     * # Аргументы
     * * `operation_type` - Тип операции (число от 0 до 10). Соответствует перечислению `ProposalsOperationType`.
     * * `proposals` - Буфер с сериализованными proposals (обычно получен от Discord).
     * * `recognized_user_ids` - Опциональный список идентификаторов пользователей, которые уже распознаны (для оптимизации).
     *
     * # Возвращает
     * Объект `ProposalsResult`, содержащий `commit` (обязателен) и `welcome` (если нужно пригласить новых участников).
     */
    processProposals(operationType: number, proposals: Buffer, recognizedUserIds?: Array<string> | undefined | null): ProposalsResult;

    /**
     * Применяет commit к состоянию группы.
     *
     * Commit обычно получается из `processProposals` или от другого участника.
     * После успешного применения группы группа переходит в новое состояние (изменяется состав или ключи).
     *
     * # Аргументы
     * * `commit` - Буфер с commit-данными.
     */
    processCommit(commit: Buffer): void;

    /**
     * Обрабатывает welcome-сообщение для вступления в группу.
     *
     * Welcome-сообщение используется новым участником для инициализации своего состояния
     * на основе данных, предоставленных существующими участниками.
     *
     * # Аргументы
     * * `welcome` - Буфер с welcome-данными.
     */
    processWelcome(welcome: Buffer): void;

    /**
     * Быстрое шифрование одного Opus-пакета (без проверки типа медиа и кодека).
     *
     * В отличие от `encrypt`, этот метод не выбрасывает исключения при ошибке,
     * а возвращает `null`. Это удобно для потоковой обработки, где потеря одного пакета допустима.
     *
     * # Аргументы
     * * `packet` - Исходный Opus-фрейм.
     *
     * # Возвращает
     * Зашифрованный пакет или `null`.
     */
    encryptOpus(packet: Buffer): Buffer | null;

    /**
     * Шифрует пачку Opus-пакетов за один вызов.
     *
     * Оптимизирует множество вызовов шифрования, уменьшая накладные расходы на пересылку между потоками.
     * Для каждого пакета в массиве возвращается соответствующий результат.
     *
     * # Аргументы
     * * `packets` - Массив исходных Opus-фреймов.
     *
     * # Возвращает
     * Массив той же длины, где каждый элемент — либо зашифрованный `Buffer`, либо `null` (если шифрование не удалось).
     */
    encryptOpusBatch(packets: Array<Buffer>): Array<Buffer | undefined | null>;

    /**
     * Расшифровывает пакет, полученный от указанного пользователя.
     *
     * # Аргументы
     * * `user_id` - Идентификатор отправителя (snowflake).
     * * `media_type` - Тип медиа (0 = Audio, 1 = Video).
     * * `packet` - Зашифрованный пакет.
     *
     * # Возвращает
     * Расшифрованный исходный пакет.
     *
     * # Ошибки
     * Если сессия не готова, пользователь не найден в группе, или аутентификация не пройдена.
     */
    decrypt(userId: string, mediaType: number, packet: Buffer): Buffer;

    /**
     * Возвращает статистику операций шифрования для всей сессии.
     *
     * Статистика включает общее количество попыток, успехов и неудач с момента последнего сброса.
     */
    getEncryptionStats(): JsEncryptionStats | null;

    /**
     * Возвращает статистику расшифрования для конкретного пользователя и аудиопотока.
     *
     * # Аргументы
     * * `user_id` - Идентификатор пользователя.
     *
     * # Возвращает
     * Статистику (успехи, неудачи, попытки, количество passthrough) или `null`, если статистика не собирается.
     */
    getDecryptionStats(userId: string): JsDecryptionStats | null;
}

/**
 * Результат обработки proposals.
 * Содержит commit, который необходимо применить, и опциональный welcome для новых участников.
 */
export interface ProposalsResult {
    /** Commit-данные (всегда присутствуют при успешной обработке). */
    commit?: Buffer;

    /** Welcome-данные (требуются, если в группу добавляются новые участники). */
    welcome?: Buffer;
}

/**
 * Статистика шифрования.
 */
export interface JsEncryptionStats {
    /** Количество успешных операций шифрования. */
    successes: number;

    /** Количество неудачных операций. */
    failures: number;

    /** Общее количество попыток шифрования. */
    attempts: number;
}

/**
 * Статистика расшифрования.
 */
export interface JsDecryptionStats {
    /** Количество успешных операций расшифрования. */
    successes: number;

    /** Количество неудачных операций. */
    failures: number;

    /** Общее количество попыток расшифрования. */
    attempts: number;

    /** Количество пакетов, пропущенных через passthrough (не расшифрованных). */
    passthroughs: number;
}

/**
 * Пара ключей подписи (асимметричная криптография).
 * Используется для аутентификации участников группы.
 */
export interface SigningKeyPair {
    /** Приватный ключ (должен храниться в секрете). */
    private: Buffer;

    /** Публичный ключ (распространяется открыто). */
    public: Buffer;
}

