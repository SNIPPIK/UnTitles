use napi::bindgen_prelude::Buffer;
use napi_derive::napi;

/// Пара ключей подписи (асимметричная криптография).
///
/// Используется для аутентификации участников группы MLS. Приватный ключ хранится в секрете,
/// публичный распространяется среди других участников через KeyPackage.
///
/// @remarks
/// Ключи должны быть сгенерированы с использованием криптостойкого генератора случайных чисел.
/// Длина ключей зависит от используемой схемы подписи (обычно Ed25519 → 32 байта каждый).
///
/// @example
/// ```ts
/// const { private, public } = crypto.generateKeyPairSync("ed25519");
/// const keyPair = { private: Buffer.from(private), public: Buffer.from(public) };
/// const session = new DAVESession(1, userId, channelId, keyPair);
/// ```
#[napi(object)]
pub struct SigningKeyPair {
  /// Приватный ключ (должен храниться в секрете, никогда не передаваться по сети).
  /// Обычно 32 байта для Ed25519.
  pub private: Buffer,

  /// Публичный ключ (распространяется открыто, входит в состав KeyPackage).
  /// Обычно 32 байта для Ed25519.
  pub public: Buffer,
}

/// Результат обработки proposals (предложений об изменении группы).
///
/// Возвращается методом `processProposals` сессии Dave. Содержит обязательный `commit`,
/// который необходимо применить через `processCommit`, и опциональный `welcome` для
/// новых участников (если добавление произошло).
///
/// @remarks
/// Если `welcome` присутствует, его следует отправить новым участникам по защищённому каналу
/// (например, через WebSocket). Без `welcome` новый участник не сможет инициализировать своё состояние.
#[napi(object)]
pub struct ProposalsResult {
  /// Commit-данные (всегда присутствуют при успешной обработке proposals).
  /// Содержит зашифрованные обновления состояния группы.
  pub commit: Option<Buffer>,

  /// Welcome-данные (требуются, если в группу добавляются новые участники).
  /// Новый участник должен вызвать `processWelcome` с этим буфером.
  pub welcome: Option<Buffer>,
}

/// Статистика операций шифрования для сессии Dave.
///
/// Собирается для всех вызовов `encrypt` (и специализированных методов `encryptOpus*`).
/// Может использоваться для мониторинга производительности и отладки.
#[napi(object)]
pub struct JsEncryptionStats {
  /// Количество успешных операций шифрования.
  pub successes: u32,

  /// Количество неудачных операций шифрования (например, из-за неготовой сессии).
  pub failures: u32,

  /// Общее количество попыток шифрования (successes + failures).
  pub attempts: u32,
}

/// Статистика операций расшифрования для конкретного пользователя и медиатипа.
///
/// Собирается для вызовов `decrypt`. Позволяет оценить качество приёма пакетов от каждого участника.
#[napi(object)]
pub struct JsDecryptionStats {
  /// Количество успешных операций расшифрования.
  pub successes: u32,

  /// Количество неудачных операций расшифрования (ошибка аутентификации, неверный ключ и т.п.).
  pub failures: u32,

  /// Общее количество попыток расшифрования (successes + failures).
  pub attempts: u32,

  /// Количество пакетов, пропущенных через passthrough-режим (расшифрование не выполнялось).
  pub passthroughs: u32,
}